export interface PalileriaConfig {
  modelo: PlModelo;
  linea: PlLinea;
  salida: PlSalida;
  colorRal: string;
  cantidad: number;
}

export interface PrecioCalculado {
  pvpUnitario: number;
  pvpTotal: number;
  costeUnitario: number;
  costeTotal: number;
  desglose: { concepto: string; importe: number }[];
  valido: boolean;
  motivoInvalido?: string;
}

const MODELO_NOMBRE: Record<PlModelo, string> = {
  pl7000: 'PL7000',
  pl7010: 'PL7010',
  pl7020: 'PL7020',
  pl7030: 'PL7030',
};

export function calcularPrecioPalilleria(c: PalileriaConfig): PrecioCalculado {
  const tarifa = TARIFAS[c.modelo];
  const pvpBase = tarifa?.[c.salida]?.[c.linea];

  if (pvpBase == null) {
    return {
      pvpUnitario: 0,
      pvpTotal: 0,
      costeUnitario: 0,
      costeTotal: 0,
      desglose: [],
      valido: false,
      motivoInvalido: `Combinación línea ${c.linea} cm × salida ${c.salida} cm no disponible para ${MODELO_NOMBRE[c.modelo]}.`,
    };
  }

  const pvpUnitario = pvpBase;
  const cantidad = Math.max(1, c.cantidad | 0);
  const pvpTotal = pvpUnitario * cantidad;
  const costeUnitario = pvpUnitario * (1 - MARGEN_DISTRIBUIDOR);
  const costeTotal = costeUnitario * cantidad;

  return {
    pvpUnitario,
    pvpTotal,
    costeUnitario,
    costeTotal,
    desglose: [
      { concepto: `Palillería ${MODELO_NOMBRE[c.modelo]} ${c.linea}×${c.salida} cm`, importe: pvpBase },
    ],
    valido: true,
  };
}
